CREATE TABLE package (
    packageid               SERIAL PRIMARY KEY,
    packagename             VARCHAR(45) NOT NULL,
    renewperiod             INTEGER NOT NULL,
    free_service            INTEGER,
    initial_free_service    INTEGER,
    subscriberprofileid     INTEGER NOT NULL,
    portspeedid             INTEGER NOT NULL,
    serviceid               INTEGER NOT NULL,
    billingtypeid           INTEGER NOT NULL,
    onetimecharge           NUMERIC(10,2) DEFAULT 0.00,
    discount                NUMERIC(10,2) NOT NULL DEFAULT 0.00,
    renewalfee              DOUBLE PRECISION DEFAULT 0,
    renewaltaxtypeid        INTEGER DEFAULT 1,
    trailpackage            INTEGER DEFAULT 0,
    active                  BOOLEAN NOT NULL DEFAULT FALSE,
    enddate                 TIMESTAMP,
    bandid                  INTEGER,
    maxvolume               DOUBLE PRECISION,
    fallbackspeed           VARCHAR(10),
    description             VARCHAR(256),
    speedinkbps             BIGINT,
    m_profile               VARCHAR(45),
    j_profile               VARCHAR(45),
    category                VARCHAR(45) DEFAULT 'REGULAR',
    plan_type               VARCHAR(45) DEFAULT 'ALL',
    parent_package          INTEGER DEFAULT 0,
    bod_min BIGINT          DEFAULT 0,
    bod_max BIGINT          DEFAULT 0,
    fbspeedinkbps           BIGINT,
    create_date             TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_date             TIMESTAMP,
    isactive                BOOLEAN NOT NULL DEFAULT TRUE,
    qos                     VARCHAR(45),
    revenueshareid          INTEGER
);